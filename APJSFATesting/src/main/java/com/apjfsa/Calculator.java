package com.apjfsa;

public class Calculator {
	
	public int add(int a,int b) {
		int add=a+b;
		return add;
	}
	public int subract(int a,int b) {
		int subract=a-b;
		return subract;
	}
	public int product(int a,int b) {
		int multiply=a*b;
		return multiply;
	}
	public int division(int a,int b) {
		int divide=a/b;
		return divide;
	}

}
